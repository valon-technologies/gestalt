type repeatedStringFlag []string

func (f *repeatedStringFlag) String() string {
	return strings.Join(*f, ",")
}

func (f *repeatedStringFlag) Set(value string) error {
	*f = append(*f, value)
	return nil
}

func run(args []string) error {
	if len(args) > 0 {
		switch args[0] {
		case "-h", "--help", "help":
			printMainUsage(os.Stderr)
			return flag.ErrHelp
		case "version", "--version", "-v":
			fmt.Println(version)
			return nil
		case "provider":
			return runProvider(args[1:])
		case "serve":
			return runServe(args[1:])
		case "init":
			return runInit(args[1:])
		case "validate":
			return runValidate(args[1:])
		case "__sandbox":
			return sandbox.RunSubcommand(args[1:])
		}
	}

	return runDefaultServe(args)
}

type serveCommandOptions struct {
	allowLocked               bool
	autoGenerateDefaultConfig bool
}

func runDefaultServe(args []string) error {
	return runServeCommand("gestaltd", printMainUsage, args, serveCommandOptions{
		autoGenerateDefaultConfig: true,
	})
}

func runServe(args []string) error {
	return runServeCommand("gestaltd serve", printServeUsage, args, serveCommandOptions{
		allowLocked: true,
	})
}

func runServeCommand(name string, usage func(io.Writer), args []string, opts serveCommandOptions) error {
	fs := flag.NewFlagSet(name, flag.ContinueOnError)
	fs.Usage = func() { usage(fs.Output()) }
	var configPaths repeatedStringFlag
	fs.Var(&configPaths, "config", "path to config file (repeat to layer overrides)")
	artifactsDir := fs.String("artifacts-dir", "", "path to writable prepared-artifacts directory")
	lockfilePath := fs.String("lockfile", "", "path to lockfile; defaults to gestalt.lock.json next to the primary config")
	var lockedFlag *bool
	if opts.allowLocked {
		lockedFlag = fs.Bool("locked", false, "require exact lock state; do not auto-init")
	}
	if err := fs.Parse(args); err != nil {
		return err
	}
	if fs.NArg() > 0 {
		return fmt.Errorf("unexpected arguments: %s", strings.Join(fs.Args(), " "))
	}

	var resolvedConfigPaths []string
	if opts.autoGenerateDefaultConfig {
		var err error
		resolvedConfigPaths, err = operator.ResolveStartConfigPaths([]string(configPaths))
		if err != nil {
			return err
		}
	} else {
		resolvedConfigPaths = operator.ResolveConfigPaths([]string(configPaths))
	}

	locked := false
	if lockedFlag != nil {
		locked = *lockedFlag
	}

	env, err := setupBootstrapWithConfigPaths(resolvedConfigPaths, operator.StatePaths{
		ArtifactsDir: *artifactsDir,
		LockfilePath: *lockfilePath,
	}, locked)
	if err != nil {
		return err
	}
	return runServer(env)
}

func runServer(env *bootstrapEnv) error {
	defer env.Close()
	return server.Run(env.Ctx, env.Config, env.Result)
}

func runInit(args []string) error {
	fs := flag.NewFlagSet("gestaltd init", flag.ContinueOnError)
	fs.Usage = func() { printInitUsage(fs.Output()) }
	var configPaths repeatedStringFlag
	fs.Var(&configPaths, "config", "path to config file (repeat to layer overrides)")
	artifactsDir := fs.String("artifacts-dir", "", "path to writable prepared-artifacts directory")
	lockfilePath := fs.String("lockfile", "", "path to lockfile; defaults to gestalt.lock.json next to the primary config")
	platformFlag := fs.String("platform", "", "additional platforms to verify hashes for (comma-separated os/arch[/libc] or \"all\")")
	if err := fs.Parse(args); err != nil {
		return err
	}
	if fs.NArg() > 0 {
		return fmt.Errorf("unexpected arguments: %s", strings.Join(fs.Args(), " "))
	}

	return initConfigWithStatePaths(configPaths, operator.StatePaths{
		ArtifactsDir: *artifactsDir,
		LockfilePath: *lockfilePath,
	}, *platformFlag)
}

func runValidate(args []string) error {
	fs := flag.NewFlagSet("gestaltd validate", flag.ContinueOnError)
	fs.Usage = func() { printValidateUsage(fs.Output()) }
	var configPaths repeatedStringFlag
	fs.Var(&configPaths, "config", "path to config file (repeat to layer overrides)")
	lockfilePath := fs.String("lockfile", "", "path to lockfile; defaults to gestalt.lock.json next to the primary config")
	if err := fs.Parse(args); err != nil {
		return err
	}
	if fs.NArg() > 0 {
		return fmt.Errorf("unexpected arguments: %s", strings.Join(fs.Args(), " "))
	}

	return validateConfig(configPaths, *lockfilePath)
}

func validateConfig(configFlags []string, lockfilePath string) error {
	scratchDir, err := os.MkdirTemp("", "gestaltd-validate-*")
	if err != nil {
		return fmt.Errorf("create validation scratch dir: %w", err)
	}
	defer func() { _ = os.RemoveAll(scratchDir) }()

	result, err := validateConfigWithStatePaths(configFlags, operator.StatePaths{
		ArtifactsDir: scratchDir,
		LockfilePath: lockfilePath,
	})
	if err != nil {
		return err
	}

	logConfigSummary(result.Paths, result.Config)
	for _, w := range result.Warnings {
		slog.Warn(w)
	}
	slog.Info("config ok")
	return nil
}

func logConfigSummary(paths []string, cfg *config.Config) {
	slog.Info("config loaded",
		"config_files", paths,
		"server_port", cfg.Server.PublicListener().Port,
		"server_public_addr", cfg.Server.PublicAddr(),
		"server_management_addr", maskEmpty(cfg.Server.ManagementAddr()),
		"server_base_url", maskEmpty(cfg.Server.BaseURL),
		"server_encryption", maskSecret(cfg.Server.EncryptionKey),
		"authentication_provider", selectedProviderLabel(cfg.SelectedAuthenticationProvider()),
		"runtime_secrets_provider", selectedProviderLabel(cfg.SelectedSecretsProvider()),
		"telemetry_provider", selectedProviderLabel(cfg.SelectedTelemetryProvider()),
	)

	for name, entry := range cfg.Plugins {
		if entry != nil {
			slog.Info("integration configured", "integration", name, "type", "plugin")
		}
	}

}

func providerEntryLabel(entry *config.ProviderEntry) string {
	switch {
	case entry == nil:
		return "(not set)"
	case entry.HasReleaseMetadataSource():
		return entry.SourceReleaseLocation()
	case entry.HasLocalSource():
		return entry.SourcePath()
	case entry.Source.IsBuiltin():
		return entry.Source.Builtin
	default:
		return "custom"
	}
}

func selectedProviderLabel(_ string, entry *config.ProviderEntry, err error) string {
	if err != nil {
		return "(invalid)"
	}
	return providerEntryLabel(entry)
}

func maskSecret(s string) string {
	if s == "" {
		return "(not set)"
	}
	return "***"
}

func maskEmpty(s string) string {
	if s == "" {
		return "(not set)"
	}
	return s
}

func printMainUsage(w io.Writer) {
	writeUsageLine(w, "Usage:")
	writeUsageLine(w, "  gestaltd [--config PATH]... [--artifacts-dir PATH] [--lockfile PATH]")
	writeUsageLine(w, "  gestaltd init [--config PATH]... [--artifacts-dir PATH] [--lockfile PATH] [--platform PLATFORMS]")
	writeUsageLine(w, "  gestaltd serve [--config PATH]... [--artifacts-dir PATH] [--lockfile PATH] [--locked]")
	writeUsageLine(w, "  gestaltd provider <command> [flags]")
	writeUsageLine(w, "  gestaltd validate [--config PATH]... [--lockfile PATH]")
	writeUsageLine(w, "")
	writeUsageLine(w, "Commands:")
	writeUsageLine(w, "  init        Resolve providers and plugins and write lock state")
	writeUsageLine(w, "  provider    Develop, validate, or build provider release archives")
	writeUsageLine(w, "  serve       Start the server (use --locked for production)")
	writeUsageLine(w, "  validate    Load and validate configuration without starting the server")
	writeUsageLine(w, "  version     Print the version and exit")
	writeUsageLine(w, "")
	writeUsageLine(w, "Flags:")
	writeUsageLine(w, "  --config          Path to a config file; repeat to layer left-to-right")
	writeUsageLine(w, "  --artifacts-dir   Path to writable prepared-artifacts directory for init/serve")
	writeUsageLine(w, "  --lockfile        Path to lockfile; defaults to gestalt.lock.json next to the primary config")
}

func printServeUsage(w io.Writer) {
	writeUsageLine(w, "Usage:")
	writeUsageLine(w, "  gestaltd serve [--config PATH]... [--artifacts-dir PATH] [--lockfile PATH] [--locked]")
	writeUsageLine(w, "")
	writeUsageLine(w, "Start the server. Auto-inits if lock state is missing or stale.")
	writeUsageLine(w, "For production, strongly prefer --locked so startup uses the pinned")
	writeUsageLine(w, "lockfile state instead of resolving new artifacts or mutating state.")
	writeUsageLine(w, "When locked, run `gestaltd init` first to prepare artifacts.")
	writeUsageLine(w, "When repeated, --config files merge left-to-right. Maps deep-merge,")
	writeUsageLine(w, "later scalars win, lists replace, and null deletes inherited keys.")
}

func printInitUsage(w io.Writer) {
	writeUsageLine(w, "Usage:")
	writeUsageLine(w, "  gestaltd init [--config PATH]... [--artifacts-dir PATH] [--lockfile PATH] [--platform PLATFORMS]")
	writeUsageLine(w, "")
	writeUsageLine(w, "Resolve remote plugin sources and write lock state.")
	writeUsageLine(w, "By default, creates gestalt.lock.json in the config directory and prepared artifacts")
	writeUsageLine(w, "in the artifacts directory. The lockfile records archive URLs for all")
	writeUsageLine(w, "discovered platforms and verified SHA256 hashes for the current platform.")
	writeUsageLine(w, "Use --platform to pre-verify hashes for additional deploy targets.")
	writeUsageLine(w, "Use this before `gestaltd serve --locked` for production deployments.")
	writeUsageLine(w, "When repeated, --config files merge left-to-right.")
	writeUsageLine(w, "")
	writeUsageLine(w, "Flags:")
	writeUsageLine(w, "  --config          Path to a config file; repeat to layer left-to-right")
	writeUsageLine(w, "  --artifacts-dir   Path to writable prepared-artifacts directory")
	writeUsageLine(w, "  --lockfile        Path to lockfile; defaults to gestalt.lock.json next to the primary config")
	writeUsageLine(w, "  --platform        Additional platforms to verify (comma-separated os/arch[/libc] or \"all\")")
}

func printValidateUsage(w io.Writer) {
	writeUsageLine(w, "Usage:")
	writeUsageLine(w, "  gestaltd validate [--config PATH]... [--lockfile PATH]")
	writeUsageLine(w, "")
	writeUsageLine(w, "Validate configuration without starting the server or running migrations.")
	writeUsageLine(w, "Validation always stages source-backed providers in a temporary scratch dir.")
	writeUsageLine(w, "Repeated --config flags merge left-to-right.")
	writeUsageLine(w, "The --lockfile path follows normal CLI path resolution; --artifacts-dir keeps")
	writeUsageLine(w, "its existing config-relative behavior on init/serve.")
}

func writeUsageLine(w io.Writer, line string) {
	_, _ = fmt.Fprintln(w, line)
}
